// Initialize gallery
function initGallery2() {
    const container = document.getElementById('thumbnailContainer2');
    
    movies2.forEach((movie, index) => {
        const div = document.createElement('div');
        div.className = `thumbnail-item-2 ${index === 0 ? 'active' : ''}`;
        div.innerHTML = `
            <img src="${movie.thumbnail}" 
                 class="thumbnail-2" 
                 alt="${movie.title}"
                 onclick="updateMovie2(${index})">
        `;
        container.appendChild(div);
    });

    updateMovie2(0);
}

function updateMovie2(index) {
    const movie = movies2[index];
    
    document.getElementById('heroImage2').src = movie.hero;
    document.getElementById('movieTitle2').textContent = movie.title;
    document.getElementById('movieDescription2').textContent = movie.description;

    document.querySelectorAll('.thumbnail-item-2').forEach((item, idx) => {
        item.classList.toggle('active', idx === index);
    });
}

function scrollGallery2(direction) {
    const container = document.getElementById('thumbnailContainer2');
    const scrollAmount = 300;
    
    if (direction === 'prev') {
        container.scrollLeft -= scrollAmount;
    } else {
        container.scrollLeft += scrollAmount;
    }
}

document.addEventListener('DOMContentLoaded', initGallery2);